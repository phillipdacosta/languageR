def main() -> None:
    for path in sorted(ROOT.glob("*.json")):
        code = path.stem
        if code == "en":
            continue
        ov = LOCALE_OVERRIDES.get(code)
        misc = MISC_LOCALE.get(code)
        if not ov:
            raise SystemExit(f"Missing LOCALE_OVERRIDES for locale: {code}")
        if not misc:
            raise SystemExit(f"Missing MISC_LOCALE for locale: {code}")
        data = json.loads(path.read_text(encoding="utf-8"))
        ps = data["PROFILE_SCREEN"]
        for k in KEYS:
            if k not in ov:
                raise SystemExit(f"{code} missing payout key {k}")
            ps[k] = ov[k]
        for k in MISC_KEYS:
            if k not in misc:
                raise SystemExit(f"{code} missing misc key {k}")
            ps[k] = misc[k]
        data["PROFILE_SCREEN"] = ps
        path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
        print("updated", code)

    print("done — payout/progress/discoverable + profile misc keys localized for all non-en locales")


def sync_mobile_profile_screen() -> None:
    """Copy payout + misc PROFILE_SCREEN keys from web i18n into mobile locales."""
    if not MOBILE_ROOT.is_dir():
        return
    for path in sorted(MOBILE_ROOT.glob("*.json")):
        code = path.stem
        web_path = ROOT / f"{code}.json"
        if not web_path.exists():
            continue
        web_ps = json.loads(web_path.read_text(encoding="utf-8")).get("PROFILE_SCREEN") or {}
        mob = json.loads(path.read_text(encoding="utf-8"))
        mob_ps = mob.get("PROFILE_SCREEN")
        if not isinstance(mob_ps, dict):
            continue
        for k in SYNC_KEYS:
            if k in web_ps:
                mob_ps[k] = web_ps[k]
        mob["PROFILE_SCREEN"] = mob_ps
        path.write_text(json.dumps(mob, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
        print("mobile", code)


if __name__ == "__main__":
    main()
    sync_mobile_profile_screen()
