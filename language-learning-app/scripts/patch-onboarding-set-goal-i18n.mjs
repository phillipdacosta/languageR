  const code = file.replace(/\.json$/, '');
  const pack = T[code];
  if (!pack) {
    console.warn('Missing SET_GOAL pack for', code);
    continue;
  }
  const p = path.join(i18nDir, file);
  const j = JSON.parse(fs.readFileSync(p, 'utf8'));
  if (!j.ONBOARDING) continue;
